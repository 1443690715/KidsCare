package com.lanbiao.youxiaoyunfamily.entity;

public class Company {
	private String company_name;
	private String RegistrationMark;
	private String company_synopsis;

	public String getCompany_name() {
		return company_name;
	}

	public void setCompany_name(String company_name) {
		this.company_name = company_name;
	}

	public String getRegistrationMark() {
		return RegistrationMark;
	}

	public void setRegistrationMark(String registrationMark) {
		RegistrationMark = registrationMark;
	}

	public String getCompany_synopsis() {
		return company_synopsis;
	}

	public void setCompany_synopsis(String company_synopsis) {
		this.company_synopsis = company_synopsis;
	}

}
