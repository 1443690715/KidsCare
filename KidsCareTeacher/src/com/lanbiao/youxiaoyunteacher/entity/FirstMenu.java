package com.lanbiao.youxiaoyunteacher.entity;

public class FirstMenu {
	private String firstname;
	private String firstid;
	private String name;
	private String studyssecondId;

	public String getFirstname() {
		return firstname;
	}

	public void setFirstname(String firstname) {
		this.firstname = firstname;
	}

	public String getFirstid() {
		return firstid;
	}

	public void setFirstid(String firstid) {
		this.firstid = firstid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getStudyssecondId() {
		return studyssecondId;
	}

	public void setStudyssecondId(String studyssecondId) {
		this.studyssecondId = studyssecondId;
	}

}
